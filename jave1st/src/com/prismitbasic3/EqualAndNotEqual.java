public class EqualAndNotEqual {

	public static void main(String[] args) {
		int firstnumber = 25;
		int secondnumber = 35;
		int thirdnumber = 35;
		if (secondnumber == thirdnumber) {
			System.out.println("secondnumber is equal to thirdnumber");
		}
		if (firstnumber != thirdnumber) {
			 System.out.println("firstnumber is not equal to thirdnumber");
		}
			

	}

}
