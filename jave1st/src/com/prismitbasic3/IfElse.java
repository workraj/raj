public class IfElse {

	public static void main(String[] args) {
		int firstnumber = 24;
		int secondnumber = 25;
		int thirdnumber = 25;
		if (firstnumber == secondnumber) {
			System.out.println("firstnumber is equal to secondnumber");
		}
		else if (secondnumber ==  thirdnumber) {
			System.out.println("secondnumber is equal to thirdnumber");
		}
		else if (firstnumber == thirdnumber) {
			System.out.println("firstnumber is equal to thirdnumber");
		}

	}

}
