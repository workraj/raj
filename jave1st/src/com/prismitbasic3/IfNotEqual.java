package com.prismitbasic3;

public class IfNotEqual {

	public static void main(String[] args) {
        int firstnumber = 25;
        int secondnumber = 26;
        int thirdnumber = 25;
        if (firstnumber != secondnumber) {
        	System.out.println("firstnumber not equal to secondnumber");
        }
        if (firstnumber == thirdnumber) {
        	System.out.println("firstnumber equal to thirdnumber");
        }
        		
	}

}
