package com.prismitbasic5;

public class JavaPattern4 {

	public static void main(String[] args) {
		int num =7;
		//int count=0;
		for(int check=0; check<=num; check++) {
			for(int check1=0;check1<=check;check1++) {
				System.out.print(""+check1);
			}
		}
		
        
        	System.out.println("");
        
	}

}
