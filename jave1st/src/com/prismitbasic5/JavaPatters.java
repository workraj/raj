package com.prismitbasic5;
/*
 * checking the java pattern
 * @raj version 1.7
 * */
public class JavaPatters {
	public static void main(String[] args) {
		int num = 9;
		for (int check=1;check<=num;check++) {
			for(int check1 =0;check1<=check;check1++) {
				System.out.print(""+check1);
			}
			System.out.println("");
		}
	}

}
